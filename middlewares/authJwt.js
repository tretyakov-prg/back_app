const jwt = require("jsonwebtoken");
const db = require("../models");
const User = db.user;
const Role = db.role;

const secret = process.env.SECRET;

verifyToken = (req, res, next) => {

  const token = req.headers["x-access-token"];

  if (!token) {
    return res.status(403).send({ error: 403, message: "No token provided!" });
  }

  jwt.verify(token, secret, (err,decode) => {
    if (err) {
      return res.status(401).send({ error: 401, message: "Unauthorized!" });
    }
    next();
  });
};

isAdmin = (req, res, next) => {
  User.findById(req.userId).exec((err, user) => {
    if (err) {
      res.status(500).send({error: 500, message: err });
      return;
    }

    Role.find(
      {
        _id: { $in: user.roles }
      },
      (err, roles) => {
        if (err) {
          res.status(500).send({error: 500, message: err });
          return;
        }

        for (let i = 0; i < roles.length; i++) {
          if (roles[i].name === "admin") {
            next();
            return;
          }
        }

        res.status(403).send({ error: 403, message: "Require Admin Role!" });
        return;
      }
    );
  });
};

